function updateTime() {
    const now = new Date();
    const time = now.toLocaleTimeString('pl-PL', { hour: '2-digit', minute: '2-digit' });
    const date = now.toISOString().split('T')[0].replace(/-/g, '.');

    if (elements.statusTime) elements.statusTime.textContent = time;
    if (elements.statusDate) elements.statusDate.textContent = date;
}

