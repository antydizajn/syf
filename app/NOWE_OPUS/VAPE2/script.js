function renderFileList() {
    const sorted = sortFiles(currentSort);
    const fileCount = sorted.length;

    elements.fileCount.textContent = fileCount;
    elements.totalSize.textContent = getTotalSize();
    elements.footerFileCount.textContent = fileCount;
    elements.footerTotalSize.textContent = getTotalSize();

    elements.fileList.innerHTML = sorted.map((item) => {
        if (item.type === 'folder') {
            return `
                <div class="file-card folder" data-slug="${item.slug}">
                    <div class="file-icon">📁</div>
                    <div class="file-name">${item.name}</div>
                    <div class="file-meta">${item.itemCount} items</div>
                </div>
            `;
        }

        return `
            <div class="file-card" data-slug="${item.slug}">
                <div class="file-icon">📄</div>
                <div class="file-name">${item.name}</div>
                <div class="file-meta">
                    ${item.modifiedDate}
                    <span class="file-size">${item.size}</span>
                </div>
            </div>
        `;
    }).join('');

    // Add click handlers
    document.querySelectorAll('.file-card').forEach(card => {
        card.addEventListener('click', () => {
            const slug = card.dataset.slug;
            const file = files.find(f => f.slug === slug);
            if (file && file.type === 'file') {
                openFile(file);
            }
        });
    });
}

function openFile(file) {
    const fileIndex = files.findIndex(f => f.slug === file.slug);
    currentFileIndex = fileIndex;

    // Update viewer
    elements.viewerFilename.textContent = file.name;
    elements.viewerDate.textContent = file.modifiedDate;

    // Render markdown
    if (typeof marked !== 'undefined') {
        elements.markdownBody.innerHTML = marked.parse(file.content || '');
    } else {
        elements.markdownBody.innerHTML = `<pre>${file.content || ''}</pre>`;
    }

    // Update nav buttons
    updateNavButtons();

    // Show viewer
    showSection('viewer');
}

function updateNavButtons() {
    const fileItems = files.filter(f => f.type === 'file');
    const currentFile = files[currentFileIndex];
    const currentFileItemIndex = fileItems.findIndex(f => f.slug === currentFile?.slug);

    elements.prevBtn.disabled = currentFileItemIndex <= 0;
    elements.nextBtn.disabled = currentFileItemIndex >= fileItems.length - 1;
}

function showSection(section) {
    // Hide all sections
    elements.filesSection.classList.add('hidden');
    elements.fileViewerSection.classList.add('hidden');
    elements.aboutSection.classList.add('hidden');

    // Show hero only on files
    const hero = document.querySelector('.hero');
    if (hero) {
        hero.style.display = section === 'files' ? 'flex' : 'none';
    }

    // Show target section
    switch (section) {
        case 'files':
            elements.filesSection.classList.remove('hidden');
            currentSection = 'files';
            break;
        case 'viewer':
            elements.fileViewerSection.classList.remove('hidden');
            currentSection = 'viewer';
            break;
        case 'about':
            elements.aboutSection.classList.remove('hidden');
            currentSection = 'about';
            break;
    }

    // Update nav active states
    updateNavActive(section === 'viewer' ? 'files' : section);

    // Scroll to top
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

function updateNavActive(section) {
    elements.navLinks.forEach(link => {
        link.classList.toggle('active', link.dataset.section === section);
    });
    elements.menuItems.forEach(item => {
        item.classList.toggle('active', item.dataset.section === section);
    });
}

// ═══════════════════════════════════════════════════════════════
// EVENT HANDLERS
// ═══════════════════════════════════════════════════════════════
function initEventListeners() {
    // Sort dropdown
    elements.sortButton?.addEventListener('click', (e) => {
        e.stopPropagation();
        elements.sortButton.parentElement.classList.toggle('open');
    });

    document.querySelectorAll('.sort-option').forEach(option => {
        option.addEventListener('click', () => {
            const sortBy = option.dataset.sort;
            currentSort = sortBy;

            // Update UI
            elements.sortValue.textContent = option.textContent;
            document.querySelectorAll('.sort-option').forEach(o => o.classList.remove('active'));
            option.classList.add('active');
            elements.sortButton.parentElement.classList.remove('open');

            // Re-render
            renderFileList();
        });
    });

    // Close dropdown on outside click
    document.addEventListener('click', () => {
        elements.sortButton?.parentElement.classList.remove('open');
    });

    // Navigation links
    elements.navLinks.forEach(link => {
        if (!link.classList.contains('exit')) {
            link.addEventListener('click', (e) => {
                e.preventDefault();
                showSection(link.dataset.section);
            });
        }
    });

    elements.menuItems.forEach(item => {
        if (!item.classList.contains('exit')) {
            item.addEventListener('click', (e) => {
                e.preventDefault();
                showSection(item.dataset.section);
                closeMobileMenu();
            });
        }
    });

    // Close viewer
    elements.closeViewer?.addEventListener('click', () => {
        showSection('files');
    });

    // Prev/Next navigation
    elements.prevBtn?.addEventListener('click', () => {
        const fileItems = files.filter(f => f.type === 'file');
        const currentFile = files[currentFileIndex];
        const currentFileItemIndex = fileItems.findIndex(f => f.slug === currentFile?.slug);

        if (currentFileItemIndex > 0) {
            openFile(fileItems[currentFileItemIndex - 1]);
        }
    });

    elements.nextBtn?.addEventListener('click', () => {
        const fileItems = files.filter(f => f.type === 'file');
        const currentFile = files[currentFileIndex];
        const currentFileItemIndex = fileItems.findIndex(f => f.slug === currentFile?.slug);

        if (currentFileItemIndex < fileItems.length - 1) {
            openFile(fileItems[currentFileItemIndex + 1]);
        }
    });

    // Copy button
    elements.copyBtn?.addEventListener('click', () => {
        const currentFile = files[currentFileIndex];
        if (currentFile?.content) {
            navigator.clipboard.writeText(currentFile.content).then(() => {
                const btn = elements.copyBtn;
                btn.textContent = '✓';
                setTimeout(() => {
                    btn.textContent = '📋';
                }, 2000);
            });
        }
    });

    // Download button
    elements.downloadBtn?.addEventListener('click', () => {
        const currentFile = files[currentFileIndex];
        if (currentFile?.content) {
            const blob = new Blob([currentFile.content], { type: 'text/markdown' });
            const url = URL.createObjectURL(blob);
            const a = document.createElement('a');
            a.href = url;
            a.download = currentFile.name;
            a.click();
            URL.revokeObjectURL(url);
        }
    });

    // Mobile menu
    elements.burger?.addEventListener('click', () => {
        elements.mobileMenu.classList.add('open');
        document.body.style.overflow = 'hidden';
    });

    elements.menuClose?.addEventListener('click', closeMobileMenu);

    // ESC closes menu/viewer
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') {
            closeMobileMenu();
            if (currentSection === 'viewer') {
                showSection('files');
            }
        }
    });

    // Window buttons (decorative close)
    document.querySelectorAll('.window-controls .close').forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            // Just a visual effect
            const window = btn.closest('.window-container');
            if (window) {
                window.style.opacity = '0';
                setTimeout(() => {
                    window.style.opacity = '1';
                }, 300);
            }
        });
    });
}

function closeMobileMenu() {
    elements.mobileMenu?.classList.remove('open');
    document.body.style.overflow = '';
}

