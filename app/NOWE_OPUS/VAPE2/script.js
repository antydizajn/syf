function formatSize(bytes) {
    if (bytes < 1024) return `${bytes} B`;
    if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
    return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
}

function getTotalSize() {
    const total = files
        .filter(f => f.type === 'file')
        .reduce((sum, f) => sum + (f.sizeBytes || 0), 0);
    return formatSize(total);
}

function sortFiles(sortBy) {
    const folders = files.filter(f => f.type === 'folder');
    const fileItems = files.filter(f => f.type === 'file');

    const sortedFolders = [...folders].sort((a, b) => a.name.localeCompare(b.name));

    let sortedFiles = [...fileItems];
    switch (sortBy) {
        case 'newest':
            sortedFiles.sort((a, b) => new Date(b.modifiedDate) - new Date(a.modifiedDate));
            break;
        case 'oldest':
            sortedFiles.sort((a, b) => new Date(a.modifiedDate) - new Date(b.modifiedDate));
            break;
        case 'name-asc':
            sortedFiles.sort((a, b) => a.slug.localeCompare(b.slug));
            break;
        case 'name-desc':
            sortedFiles.sort((a, b) => b.slug.localeCompare(a.slug));
            break;
    }

    return [...sortedFolders, ...sortedFiles];
}

